import enum


class DefaultLocation(enum.StrEnum):
    UNKNOWN = "UNKNOWN"
    CUTONALA = "CUTONALA"
    CUCEI = "CUCEI"
    CUAAD = "CUAAD"
    CUCSH = "CUCSH"
    CUCS = "CUCS"
    CUGDL = "CUGDL"
    CUCBA = "CUCBA"
    CUTLAJO = "CUTLAJO"
    CUCEA = "CUCEA"


DEFAULT_LOCATION = DefaultLocation.CUTONALA


def get_gps_from_location(location: DefaultLocation) -> tuple[float, float] | None:
    match_case = {
        DEFAULT_LOCATION.CUTONALA: (20.566685233669638, -103.228642206316),
        DEFAULT_LOCATION.CUCEI: (20.654911257758222, -103.32543897057933),
        DEFAULT_LOCATION.CUAAD: (20.740595363459207, -103.31218755922237),
        DEFAULT_LOCATION.CUCSH: (20.741588328791167, -103.37639382883754),
        DEFAULT_LOCATION.CUGDL: (20.694307641713777, -103.34974854775403),
        DEFAULT_LOCATION.CUCBA: (20.746578861788336, -103.5137309423387),
        DEFAULT_LOCATION.CUTLAJO: (20.46453551066533, -103.41360925822853),
        DEFAULT_LOCATION.CUCEA: (20.74079816228851, -103.38092099979947),
    }

    if gps := match_case[location]:
        return gps

    return None
